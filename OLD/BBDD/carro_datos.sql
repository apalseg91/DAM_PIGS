AFTER UPDATE OF activo ON CLIENTE
FOR EACH ROW
 WHEN (OLD.activo IS NOT NULL AND NEW.activo IS NOT NULL AND OLD.activo != NEW.activo) DECLARE
BEGIN
  -- Cerrar estado anterior (poner fecha_fin)
  UPDATE CLIENTE_ESTADO
  SET fecha_fin = SYSDATE
  WHERE id_cliente = :OLD.id_cliente
    AND fecha_fin IS NULL;

  -- Insertar nuevo estado
  INSERT INTO CLIENTE_ESTADO (id_estado, id_cliente, fecha_inicio, activo)
  VALUES (SEQ_CLIENTE_ESTADO.NEXTVAL, :NEW.id_cliente, SYSDATE, :NEW.activo);
END;

